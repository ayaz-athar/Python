friends=["Apple","Orange",5,432.04,False,"Ayaz","Verizox"]
print(friends)

friends.append("Mango") #append method adds an element to the end of the list
print(friends)

friends.insert(2,"Banana") #insert method adds an element at a specific index
print(friends)

friends.remove("Orange") #remove method removes the first occurrence of the specified element
print(friends)

friends.pop() #pop method removes the last element of the list
print(friends)


friends.reverse() #reverse method reverses the order of the list
print(friends)

friends.clear() #clear method removes all elements from the list
print(friends)

